/**
 * Утилиты для генерации URL-friendly slug
 */

// Таблица транслитерации кириллицы в латиницу
const translitMap: Record<string, string> = {
  'а': 'a', 'б': 'b', 'в': 'v', 'г': 'g', 'д': 'd', 'е': 'e', 'ё': 'yo',
  'ж': 'zh', 'з': 'z', 'и': 'i', 'й': 'y', 'к': 'k', 'л': 'l', 'м': 'm',
  'н': 'n', 'о': 'o', 'п': 'p', 'р': 'r', 'с': 's', 'т': 't', 'у': 'u',
  'ф': 'f', 'х': 'h', 'ц': 'ts', 'ч': 'ch', 'ш': 'sh', 'щ': 'sch', 'ъ': '',
  'ы': 'y', 'ь': '', 'э': 'e', 'ю': 'yu', 'я': 'ya',
  'А': 'A', 'Б': 'B', 'В': 'V', 'Г': 'G', 'Д': 'D', 'Е': 'E', 'Ё': 'Yo',
  'Ж': 'Zh', 'З': 'Z', 'И': 'I', 'Й': 'Y', 'К': 'K', 'Л': 'L', 'М': 'M',
  'Н': 'N', 'О': 'O', 'П': 'P', 'Р': 'R', 'С': 'S', 'Т': 'T', 'У': 'U',
  'Ф': 'F', 'Х': 'H', 'Ц': 'Ts', 'Ч': 'Ch', 'Ш': 'Sh', 'Щ': 'Sch', 'Ъ': '',
  'Ы': 'Y', 'Ь': '', 'Э': 'E', 'Ю': 'Yu', 'Я': 'Ya'
};

/**
 * Транслитерация текста с кириллицы на латиницу
 */
export function transliterate(text: string): string {
  return text
    .split('')
    .map(char => translitMap[char] || char)
    .join('');
}

/**
 * Генерация URL-friendly slug из текста
 * 
 * - Транслитерирует кириллицу в латиницу
 * - Приводит к нижнему регистру
 * - Заменяет пробелы и специальные символы на дефисы
 * - Удаляет множественные дефисы
 * - Обрезает дефисы в начале и конце
 * 
 * @example
 * generateSlug("Миниинвазивная хирургия / Молочная железа - 2026")
 * // => "miniinvazivnaya-hirurgiya-molochnaya-zheleza-2026"
 */
export function generateSlug(text: string): string {
  if (!text) return '';
  
  return transliterate(text)
    .toLowerCase()
    // Заменяем специальные символы и разделители на дефисы
    .replace(/[\/\\\-–—_\s]+/g, '-')
    // Удаляем все символы кроме букв, цифр и дефисов
    .replace(/[^a-z0-9-]/g, '')
    // Заменяем множественные дефисы на один
    .replace(/-+/g, '-')
    // Убираем дефисы в начале и конце
    .replace(/^-+|-+$/g, '')
    // Ограничиваем длину (опционально, для читабельности)
    .substring(0, 100);
}

/**
 * Проверка, является ли строка валидным UUID v4
 */
export function isUUID(str: string): boolean {
  const uuidRegex = /^[0-9a-f]{8}-[0-9a-f]{4}-4[0-9a-f]{3}-[89ab][0-9a-f]{3}-[0-9a-f]{12}$/i;
  return uuidRegex.test(str);
}

/**
 * Валидация slug
 * - Только строчные латинские буквы, цифры и дефисы
 * - Не начинается и не заканчивается дефисом
 * - Минимум 2 символа
 */
export function isValidSlug(slug: string): boolean {
  if (!slug || slug.length < 2) return false;
  // Только строчные буквы, цифры и дефисы, не начинается/заканчивается дефисом
  const slugRegex = /^[a-z0-9]+(?:-[a-z0-9]+)*$/;
  return slugRegex.test(slug);
}
